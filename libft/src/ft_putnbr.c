#include "libft.h"

void	ft_putnbr(int n)
{
	if (n > 9)
		ft_putnbr(n / 10);
	if (n >= 0)
		ft_putchar((n % 10) + '0');
	else
	{
		ft_putchar('-');
		if (n == -2147483648)
		{
			ft_putstr("2147483648");
			return ;
		}
		ft_putnbr(-n);
	}
}
